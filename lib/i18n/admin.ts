export function normalizeAdminLocale(input?: string | null): SupportedLocale {
  const value = String(input || "").toLowerCase()
  if (value === "pt" || value.startsWith("pt-br")) return "pt-BR"
  if (value === "es" || value.startsWith("es-")) return "es"
  if (value === "ko" || value.startsWith("ko-")) return "ko"
  if (value === "zh" || value.startsWith("zh-")) return "zh"
  return "en"
}

export function tAdmin(localeInput: string | null | undefined, key: string, fallback?: string): string {
  const locale = normalizeAdminLocale(localeInput)
  const localeMessages = MESSAGES[locale] || {}
  return localeMessages[key] ?? MESSAGES.en[key] ?? fallback ?? key
}
